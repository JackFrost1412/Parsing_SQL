		AR_DIM.AR_DIM_ID		AR_DIM_ID,
		TP.AR_TP_DIM_ID         AR_TP_DIM_ID,
		NVL(CST_DIM.CST_TP, 'UNKNOWN' )		CST_TP
FROM AR_DIM
INNER JOIN CSTB_SYSTEM SYS ON SYS.PARAM_NAME = 'ETL_DATE' 
INNER JOIN AR_TP_DIM TP ON TP.LVL_0_NM_ENG = AR_DIM.AR_TP
INNER JOIN CST_DIM ON CST_DIM.CST_ID = AR_DIM.CST_ID AND CST_DIM.EFF_FM_DT <= TO_DATE(SYS.PARAM_VALUE, 'YYYYDDD')  AND CST_DIM.EFF_TO_DT > TO_DATE(SYS.PARAM_VALUE, 'YYYYDDD')
WHERE SRC_STM IN ('SIBS_CDMAST', 'SIBS_DDMAST','SIBS_LNMAST', 'SIBS_ODTIER','CADENCIE_ACCT','SIBS_TFMAST','TTSPKB')
AND AR_DIM.EFF_FM_DT <= TO_DATE(SYS.PARAM_VALUE, 'YYYYDDD')  AND AR_DIM.EFF_TO_DT > TO_DATE(SYS.PARAM_VALUE, 'YYYYDDD');

